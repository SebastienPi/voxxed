package org.jduchess.microservices;

public class Seller {
    private int id;
    private String name;

    public Seller(){}

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
